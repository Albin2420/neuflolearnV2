import 'package:firebase_auth/firebase_auth.dart';
import 'package:neuflo_learn/src/data/services/firebase/firebase_auth_impl.dart';

abstract class Auth {
  User? getCurrentUser();
  Stream<User?> get onAuthStateChanged;
  Stream<User?> get onUserChanges;

  Future<AuthStatus?> signInWithGoogle();
  Future googleSignOut();
  Future updateUserPhoneNumber();
  Future signOut();
  Future refreshFirebaseToken();
  Future refreshUser();
}
